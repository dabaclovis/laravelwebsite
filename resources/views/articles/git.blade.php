        <p>1.) Version Control are tools that help software team manage changes in source code over time</p>
        <p> For most almost all software projects, the source code is like the crown KING - a precious asset whose
            value must be protected.
        </p>
        <p>2.) Git is a version control system sometime referred to as Source Code Management (SCM)</p>
        <p>3.) There are two type of version control:
        <ul>
            <li>Centralized version control and</li>
            <li>Distributed version control</li>
        </ul>
        Git is a Distributed version control.</p>
        <p>4.) Git bash is a terminal for executing git command same a the command line in window</p>
        <p>5.) Got to this <a href="https://git-scm.com/downloads" target="_blank">website</a> and download git for your
            operating system.</p>
        <p>Then follow the onscreen instructions to install git on your system.</p>
        <p>6.)There a three types of files in git:
        <ul>
            <li>untracked files</li>
            <li>staged files and </li>
            <li>commited files</li>
        </ul>
        </p>
        <p>7.) Create a folder on your computer and right-click on the created folder and select
            <i>git bash here</i>. The git terminal will pop up like a modal.
        </p>
        <p>7.) Initiate the directory as git repository by typing <i>git init</i>. Then you can add your name and email
            with
            the following command</p>
        <p><i>git config --global user.name 'your name'</i></p>
        <p><i>git config --global user.email 'youremail@.....com'</i></p>
        <p>To checked the configuration type <i>git config --global --list</i></p>
        <p>8.)Frequently used git commands
        <ul>
            <li><i>git init</i></li>
            <li><i>git add</i> single file </li>
            <li><i>git add .</i> multiple files</li>
            <li><i>git commit -m 'value' => message</i></li>
            <li><i>git remote add origin url of remote github repository</i></li>
            <li><i>git push -u origin main or master</i></li>
        </ul>
        </p>
